public class JakasKlasa {
    int cos;
    int co1;
    int co3;
    int co4;
    int co5;

    public JakasKlasa(int cos, int co1, int co3, int co4, int co5) {
        this.cos = cos;
        this.co1 = co1;
        this.co3 = co3;
        this.co4 = co4;
        this.co5 = co5;
    }

    public String toString()
    {
        return (this.cos+" "+this.co1+" "+this.co3+" "+this.co4+" "+this.co5);
    }
}
